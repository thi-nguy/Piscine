#include <unistd.h>

int		aff_a(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == 'a')
		{
			write(1, "a\n", 2);
			return (0);
		}
		i++;
	}
	write(1, "\n", 1);
	return (0);
}

int		main(int ac, char **av)
{
	if (ac != 2)
		write(1, "a\n", 2);
	else
		aff_a(av[1]);
	return (0);
}
