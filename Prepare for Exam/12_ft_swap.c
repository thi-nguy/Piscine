#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int t;

	t = *a;
	*a = *b;
	*b = t;
}

int main (void)
{
	int a = 23;
	int b = 54;
	ft_swap(&a, &b);
	printf("%d is a\n", a);
	printf("%d is b", b);
	return (0);
}
